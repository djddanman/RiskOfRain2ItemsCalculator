class Survivor:
    def __init__(self, name, health, health_per_level, regen, regen_per_level, damage, damage_per_level, speed, armor):
        self.name = name
        self.health = health
        self.health_per_level = health_per_level
        self.regen = regen
        self.regen_per_level = regen_per_level
        self.damage = damage
        self.damage_per_level = damage_per_level
        self.speed = speed
        self.armor = armor

